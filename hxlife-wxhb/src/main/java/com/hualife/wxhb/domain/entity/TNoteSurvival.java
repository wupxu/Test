package com.hualife.wxhb.domain.entity;

public class TNoteSurvival {
    private String survivalNo;

    private String survivalName;

    private String survivalBranchCode;

    private String survivalBranchName;

    public String getSurvivalNo() {
        return survivalNo;
    }

    public void setSurvivalNo(String survivalNo) {
        this.survivalNo = survivalNo;
    }

    public String getSurvivalName() {
        return survivalName;
    }

    public void setSurvivalName(String survivalName) {
        this.survivalName = survivalName;
    }

    public String getSurvivalBranchCode() {
        return survivalBranchCode;
    }

    public void setSurvivalBranchCode(String survivalBranchCode) {
        this.survivalBranchCode = survivalBranchCode;
    }

    public String getSurvivalBranchName() {
        return survivalBranchName;
    }

    public void setSurvivalBranchName(String survivalBranchName) {
        this.survivalBranchName = survivalBranchName;
    }
}